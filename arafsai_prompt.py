import requests

async def get_current_city():
    try:
        response = requests.get("https://ipinfo.io", timeout=5)
        data = response.json()
        return data.get("city", "Unknown")
    except Exception as e:
        print(f"Error getting current city: {e}")
        return "Unknown"

